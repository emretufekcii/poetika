<h6>1953 İhtiyar Balıkçı (Yaşlı Adam ve Deniz)</h6>
“Talih satıldığı bir yer varsa gidip biraz satın almak isterdim. Peki neyle alacağım? Yitmiş bir zıpkın, kırılmış bir bıçak, işe yaramayan iki elle mi? “Neden olmasın? Seksendört gününü vererek onu satın almak için çalışıp çabaladın. Neredeyse alıyordun da. Nen var senin? Bu aptalca düşünceler de neyin nesi? Talih öyle bir şeydir ki herkesin karşısına binbir değişik şekilde çıkar da tanımaz onu insan. Onu tanımak için biraz sağgörülü olmak lazım.”
<hr>
<h6> 1961 Bülbülü Öldürmek </h6>
Bülbülü Öldürmek, Harper Lee’nin 1960’ta yayınlanan Pulitzer ödüllü romanı. Yayınlandığı dönemde büyük bir başarı kazanarak, modern Amerikan Edebiyatı’nın klasikleri arasına girdi. Roman, yazarın 1936 yılında, on yaşındayken yaşadığı bir olayı temel almaktadır.
<hr>
 <h6>1980 Alıklar Birliği</h6>
Amerikalı romancı John Kennedy Toole’un 1963’te kaleme aldığı romanı. Ne var ki roman yazarın intiharından 11 yıl sonra yayınlanabilmiş ve 1981 yılında Pulitzer ödülüne layık görülmüştür. Alıklar Birliği konusu ise şu şekilde :
<hr>
Alıklar Birliği’nin kahramanı obur, aksi, tembel, bencil, her şeye karşı, her şeyden hoşnutsuz, toplum düşmanı İgnatius. Annesi mutlaka bir iş bulup çalışması gerektiğini söylüyor, kız arkadaşı cinsel güdülerini serbest bırakırsa bütün sorunlarının çözüleceğini düşünüyor. Ama tamamen eşcinsellerden kurulan ordularla dünyanın barış dolu bir yer olacağını iddia edip bunu gerçekleştirmek üzere eşcinselleri örgütlemeye kalkışmak gibi tuhaf girişimlerin adamı olan İgnatius, onlara ve modern zamanlara inat, geğirerek, yellenerek ve homurdanarak, bıkmadan usanmadan çağının her türlü aşırılığına sövüyor…
<hr>
 <h6>1996 Angela’nın Külleri </h6>
Angela‘nın Külleri, İrlanda asıllı Amerikalı öğretmen ve yazar Frank McCourt’un 1996 yılında yazdığı Pulitzer Ödüllü otobiyografik kitabının adıdır. Özgün adı Angela’s Ashes’dir. Pek çok dilde defalarca basılan bu anı kitabına birçok ödül verildi.
<hr>
<h6>2008 Kül Mevsimi</h6>
Elizabeth Strout’un Pulitzer ödüllü çok satan kitabı Kül Mevsimi, bütün kusurlarına rağmen olağanüstü bir insancıllığa sahip kahramanı Olive Kitteridge’in eşliğinde okurlarını sıradan insanların gündelik detayların büyüsüyle örülü yaşamlarında bir gezintiye çıkarıyor.
<hr>
Bazen inatçı, bazen sabırlı, bazen anlayışlı bazense hazin bir inkâr içindeki Olive Kitteridge bilerek ya da bilmeyerek birçok hayata dokunur: Olive’in aşırı hassaslığından bıkmış bir oğul; evliliğine olan sadakati hem bir lütuf hem de bir lanet olan bir koca; geçmişte yaşadığı bir aşk macerası peşini bırakmayan bir piyanist; yaşama isteğini kaybetmiş eski bir öğrenci…
