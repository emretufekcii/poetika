<h6> 1939 Gazap Üzümleri</h6>
John Steinbeck’in ünlü romanı Gazap Üzümleri. Bu romanında yazar, Amerika’da 1930’lu yılların ekonomik kriz dönemlerini, insanlığın dramını etkileyici bir dille anlatmaktadır. Joad ailesinin özelinden, genele yansıyan bakış açısıyla emekçi insanları konu alan kitap, dünyanın önde gelen ve en çok okunan klasiklerinden biridir.
