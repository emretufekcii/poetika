					<p class="card-text lead"> <h6> 1921 Masumiyet Çağı</h6>
Amerikalı yazar Edith Wharton”un 1920 yılında yayınladığı romanıdır. Masumiyet Çağı konusu 1870’lerde New York şehrinde geçer.  Üst sınıf bir çiftin yaklaşmakta olan evliliği ve gelinin, mutluluklarını tehdit eden kuzeni romanda konu edilir ve Amerikan toplumunda iki yüzlü yaşamı, dejenere olmaya başlayan Amerikan sosyetesi, dedikodu ve ahlaksız erkeklerle kadınları anlatılır.
